    private void botonNovaPartidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonNovaPartidaActionPerformed
        String[] opcion = {"Un xogador, xerando a palabra ao azar", "Dous xogadores, introducindo a palabra a adiviñar", "Xogo por defecto"}; 
        Object jugadores = JOptionPane.showInputDialog(
                  null, // ventaá pai
                 "Seleccione un modo de xogo", // mensaxe
                 "Modo de xogo", // titulo
                  JOptionPane.QUESTION_MESSAGE, // tipo mensaxe
                  null, // icono
                  opcion, // valores
                  opcion[0] // valor marcado inicialmente
                  );
        
        if (jugadores == opcion[0]) {
            jugador1();
        } else if (jugadores == opcion[1]) {
            jugador2();
        } else if (jugadores == opcion[2]) {
            jugador3();
        }
    }//GEN-LAST:event_botonNovaPartidaActionPerformed
    //Se crea un objeto aforcado para mostrar la palabra a adivinar que se leerá de un fichero
    private void jugador1() {
        try {
            aforcado = new Aforcado(new XeradorPalabraFicheiro().xerarPalabra());
            activarBotones(); //Se activan los botones de las letras
            letra.setText("");
            reiniciarAhorcado(); //Se muestra la primera capa del ahorcado
            palabraConGuiones.setText(aforcado.amosarPalabraOculta()); //Se muestra la palabra oculta
            errores.setText(aforcado.getStringErros());//Se muestran los errores
            letra.requestFocus(); //Se coloca el cursor en el panel donde se introduce la letra manualmente
            //Si no se encuentra el fichero, el juego entra en modo por defecto.    
        } catch (XerarPalabraExcepcion ex) {
            JOptionPane.showMessageDialog(this, "Ficheiro coas palabras non atopado.\n"
                    + "Xogando coas palabras por defecto", "AVISO", JOptionPane.WARNING_MESSAGE);
            aforcado = new Aforcado(new XeradorArrayPalabras().xerarPalabra());
            activarBotones();
            letra.setText("");
            reiniciarAhorcado();
            palabraConGuiones.setText(aforcado.amosarPalabraOculta());
            errores.setText(aforcado.getStringErros());
            letra.requestFocus();
        }
    }
