    //Método que contiene otros métodos del juego
    private void amosarEstadoXogo() {
        mostrarPalabras();
        mostrarErros();
        finDoXogo();
        letra.setText("");
    }
    //Método que comprueba que la letra está en una expresión regular
    private void probarCaracter(String letraAProbar) {
        letraAProbar = letraAProbar.toLowerCase();
        if (letraAProbar.isEmpty() || !letraAProbar.matches("[a-zñáéíóúü]+")) {
            JOptionPane.showMessageDialog(this, "Introduza só letras da a á z","Error", JOptionPane.WARNING_MESSAGE);
            letraAProbar = null;
        } else if (aforcado.getStringErros().contains(letraAProbar)) {
            letra.setText("");
            JOptionPane.showMessageDialog(this, "A letra xa foi introducida","Error", JOptionPane.WARNING_MESSAGE);
        } else {
            aforcado.comprobarCaracter(letraAProbar.charAt(0));
        }
        palabraConGuiones.setText(aforcado.amosarPalabraOculta());
    }
    
    //Método que muestra la palabra oculta y los errores
    private void mostrarPalabras() {
        palabraConGuiones.setText(aforcado.amosarPalabraOculta());
        errores.setText(aforcado.getStringErros());
    }
    
    //Método que va mostrando las capas con las imágenes del ahorcado
    private void mostrarErros() {
        switch (aforcado.getErros().size()) {
            case 1:
            cabeza.setVisible(true);
            break;
            case 2:
            tronco.setVisible(true);
            break;
            case 3:
            brazoIzquierdo.setVisible(true);
            break;
            case 4:
            brazoDerecho.setVisible(true);
            break;
            case 5:
            pierna.setVisible(true);
            break;
            case 6:
            Ahorcado.setVisible(true);
            break;
        }
    }
    
    //Método que muestra el final de la partida
    private void finDoXogo() {
        if (aforcado.eFinXogo()) {
            letra.setText("");
            desactivarBotones();
            if (aforcado.maxErrosSuperado()) {
                JOptionPane.showMessageDialog(this, "Quedaches aforcado!!\nA palabra era " + aforcado.amosarPalabraCompleta(), "FIN DO XOGO", JOptionPane.WARNING_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "PARABENS!!\nA palabra era " + aforcado.amosarPalabraCompleta(), "FIN DO XOGO", JOptionPane.WARNING_MESSAGE);
            }
        }
    }
    
